    <p>Breast cancer is a disease that starts in the breast with a malignant tumor. A malignant tumor is a mass of cells that grows out of control.
      The cancerous cells can also metastasize, or move to other tissues or parts of the body.<br>

      The cancer can develop in any of the three types of breast tissue: lobules, ducts, and connective tissue.<br>

Most cancer begins in the lobules (the milk-producing glands), or in the ducts, along which milk travels to the nipple.<br>
 (1) But tumors can also develop in the fibrous and fatty connective tissue that surrounds the lobules and ducts.

Several different types of breast cancer exist. The type of breast cancer and its stage, or how far it has grown, determine the treatment for it.<br>

Breast cancer that spreads into normal tissue is called invasive breast cancer. Noninvasive breast cancer stays within the breast lobule or duct. (2)

